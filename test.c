int test_key_expansion() {
	printf("Testing key_expansion()...");
	uint8_t round_key[] = {
		0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
		0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f
	};
	uint8_t expected[] = {0xd6, 0xaa, 0x74, 0xfd};
	uint8_t new_key[] = {
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
	};
	bb_key_expansion(round_key, sizeof(round_key), new_key, 1);
	if (memcmp(expected, new_key, sizeof(expected)) != 0) {
		printf(STR_FAILED);
		return 1;
	} else {
		printf(STR_PASSED);
		return 0;
	}
}

int test_key_expansion_ex(uint8_t* key, uint8_t key_size, uint8_t* expected, uint8_t rounds) {
	for (int i = 0; i < rounds; i++) {
		bb_key_expansion(key, key_size, key, i + 1);
		if (memcmp(key, expected + (i * key_size), key_size) != 0) {
			printf(STR_FAILED);
			return 1;
		}
	}

	printf(STR_PASSED);
	return 0;
}

